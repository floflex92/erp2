// ── Helper : ajouter un header coloré ──
function styleHeader(ws, colorKey) {
  const c = COLORS[colorKey] || COLORS.divers;
  ws.getRow(1).eachCell(cell => {
    cell.font = { bold: true, color: { argb: 'FFFFFFFF' }, size: 11 };
    cell.fill = { type: 'pattern', pattern: 'solid', fgColor: { argb: 'FF' + c.font } };
    cell.alignment = { horizontal: 'center', vertical: 'middle', wrapText: true };
    cell.border = {
      bottom: { style: 'thin', color: { argb: 'FF999999' } }
    };
  });
  ws.autoFilter = { from: 'A1', to: { row: 1, column: ws.columns.length } };
  ws.views = [{ state: 'frozen', ySplit: 1 }];
}

function addSheet(name, colorKey, tables) {
  const ws = wb.addWorksheet(name, {
    properties: { tabColor: { argb: 'FF' + COLORS[colorKey]?.font || '666666' } }
  });
  ws.columns = [
    { header: 'Table', key: 'table', width: 32 },
    { header: 'Colonne', key: 'col', width: 32 },
    { header: 'Type', key: 'type', width: 18 },
    { header: 'Contraintes', key: 'constraints', width: 40 },
    { header: 'FK →', key: 'fk', width: 30 },
    { header: 'Description', key: 'desc', width: 40 },
  ];

  for (const t of tables) {
    for (const col of t.columns) {
      const row = ws.addRow({
        table: col === t.columns[0] ? t.name : '',
        col: col.name,
        type: col.type,
        constraints: col.constraints || '',
        fk: col.fk || '',
        desc: col.desc || '',
      });
      if (col === t.columns[0]) {
        row.getCell('table').font = { bold: true, size: 10 };
      }
      row.getCell('col').font = { size: 10 };
    }
    // Ligne vide entre tables
    ws.addRow({});
  }

  styleHeader(ws, colorKey);
  return ws;
}

