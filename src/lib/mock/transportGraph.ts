const assignmentMap = new Map(MOCK_TRANSPORT_ASSIGNMENTS.map(assignment => [assignment.id, assignment]))

export function resolveMissionResources(mission: Pick<MockTransportMissionRef, 'assignmentId'>): MissionResources {
  const assignment = mission.assignmentId ? assignmentMap.get(mission.assignmentId) ?? null : null
  return {
    conducteurId: assignment?.conducteurId ?? null,
    vehiculeId: assignment?.vehiculeId ?? null,
    remorqueId: assignment?.remorqueId ?? null,
  }
}

export function getMissionStepStatuses(mission: Pick<MockTransportMissionRef, 'statut' | 'statutOperationnel'>) {
  if (mission.statutOperationnel === 'termine' || mission.statut === 'livre' || mission.statut === 'facture') {
    return { chargement: 'realise', livraison: 'realise' }
  }

  if (mission.statut === 'en_cours') {
    return { chargement: 'realise', livraison: 'en_cours' }
  }

  return { chargement: 'en_attente', livraison: 'en_attente' }
}
